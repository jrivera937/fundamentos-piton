x = input("Ingrese su lista de n√∫meros separados por espacio: ")
lista = x.strip().split()
sin_duplicados = []
for i in lista:
    if i not in sin_duplicados:
        sin_duplicados.append(i)
print(" ".join(sin_duplicados))