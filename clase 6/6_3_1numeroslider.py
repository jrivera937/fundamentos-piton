x = input("Ingrese su lista de nÃºmeros separados por espacio: ")
numeros = [int(n) for n in x.split()]
lideres = [numeros[-1]]

for i in range(len(numeros) - 2, -1, -1):
    if numeros[i] > lideres[0]:
        lideres.insert(0, numeros[i])

print(*lideres)